package eeob.edutec.promediar;

/**
 *
 * @author Erick Ore
 */
public class PromedNumeros {
    public double PromedNumeros(double n1,double n2){
            double promedio=(n1+n2)/2;
            return promedio;
        }
        public double PromedNumeros(double n1,double n2, double n3){
            double promedio=(n1+n2+n3)/3;
            return promedio;
        }
        public double PromedNumeros(double n1,double n2, double n3, double n4){
            double promedio=(n1+n2+n3+n4)/4;
            return promedio;
        }        
        public double PromedNumeros(double n1,double n2, double n3, double n4, double n5){
            double promedio=(n1+n2+n3+n4+n5)/5;
            return promedio;
        }             
}
